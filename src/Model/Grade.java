package Model;

public class Grade {
	private String sno;
	private String cid;
	private String grade;
	
	public String getSno() {
		return this.sno;
	}
	
	public String getCid() {
		return this.cid;
	}
	
	public String getGrade() {
		return this.grade;
	}
	
	public void setSno(String sno) {
		this.sno=sno;
	}
	
	public void setCid(String cid) {
		this.cid=cid;
	}
	public void setGrade(String grade) {
		this.grade=grade;
	}
}
